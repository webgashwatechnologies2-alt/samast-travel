// Helper: get all packages for a destination slug
export function getPackagesByDestination(destSlug) {
  return Object.values(allPackages)
    .filter((p) => p.destination === destSlug)
    .map((p) => ({ ...p, image: packageImages[p.slug] || p.image }));
}

// Helper: get a single package by slug
export function getPackageBySlug(slug) {
  const p = allPackages[slug] || null;
  if (!p) return null;
  return { ...p, image: packageImages[slug] || p.image };
}

// Helper: all package slugs (for static generation)
export function getAllPackageSlugs() {
  return Object.keys(allPackages);
}
